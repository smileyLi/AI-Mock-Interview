from typing import List


def build_summary_resume_addon(resume_text: str, max_chars: int) -> str:
    """结束面试写总结时可选注入，便于对照简历做岗位匹配等评估。"""
    t = (resume_text or "").strip()
    if not t:
        return ""
    if len(t) > max_chars:
        t = t[:max_chars] + "\n（以下内容已截断）"
    return "【供评估参考的候选人简历摘录】\n" + t


