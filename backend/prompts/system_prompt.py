def build_resume_addon(resume_text: str) -> str:
    """第一阶段注入：基于简历提问。"""
    t = (resume_text or "").strip()
    if not t:
        return ""
    return (
        "【候选人简历摘录】以下为候选人上传的简历解析文本。"
        "在进入题库环节之前，热身提问请务必结合其中的项目经历、技能栈与教育背景展开，可追问细节；不要脱离简历空泛提问。\n\n"
        + t
    )


def build_question_bank_addon(
    bank_question_texts: List[str],
    current_question_index: int,
    bank_turns_on_current: int,
    max_replies_per_question: int,
) -> str:
    """题库环节注入：三道题材料 + 当前聚焦题与追问兜底说明。"""
    n = len(bank_question_texts)
    if n == 0:
        return ""

    lines = [
        "【题库环节】下面是从题库随机抽取的结构化题目，共"
        + str(n)
        + "道。请你围绕当前聚焦的题目深入追问，认为候选人回答充分后再自然过渡到下一题；"
        "口语化提问，不要朗读原文。",
        "",
    ]
    for i, text in enumerate(bank_question_texts):
        lines.append(f"—— 第{i + 1}题 ——")
        lines.append(text)
        lines.append("")

    cur = min(max(current_question_index, 0), n - 1)
    lines.append(
        f"当前必须优先考察：第{cur + 1}题。本题内候选人已累计发言约 {bank_turns_on_current} 轮；"
        f"若已超过约 {max_replies_per_question} 轮仍未充分收束，你应主动收口并切换到下一题。"
        f"在完成第{n}题前不要结束整场技术考察。"
    )
    return "\n".join(lines)


